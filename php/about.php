			<p class="color">ஸ்ரீராமகிருஷ்ண விஜயம்</p>
			<p>ஸ்ரீராமகிருஷ்ண விஜயம், ஆன்மிக-பண்பாட்டு மாத இதழ் சென்னை ஸ்ரீராமகிருஷ்ண மடத்திலிருந்து வெளியிடப்படுகிறது. 1921, ஜனவரி 14-ஆம் நாள் பொங்கல் திருநாளன்று இந்த இதழ் துவங்கப்பட்டது. தை பிறந்தால் வழி பிறக்கும் என்னும் முதுமொழிக்கிணங்கப் பக்தர்களுக்கு ஆன்மிக வழியைக் காட்டும் பணியை விஜயம் அன்று தொடங்கியது. </p>
			<p id="more_tamil">ஸ்ரீராமகிருஷ்ண விஜயம் - தமிழகத்தின் மூத்த ஆன்மிக இதழ். தனது 92-வது வருடத்தில் நூற்றாண்டை நோக்கி வெற்றி நடைபோட்டு வருகிறது. 2007-இல் ஒரு லட்சம் பிரதிகளைத் தாண்டிய இதன் சர்குலேசன் தற்போது 1,50,000 பிரதிகளை எட்டியுள்ளது. </p>
			<p>ஸ்ரீராமகிருஷ்ணர், ஸ்ரீசாரதாதேவி மற்றும் சுவாமி விவேகானந்தரின் கருத்துகளைத் தாங்கி வரும் விஜயம் இதழ், அகில உலகமும் சுவாமி விவேகானந்தரின் 150-வது பிறந்த ஆண்டைக் கொண்டாடும் இந்த நேரத்தில் 150 ஆயிரம் பிரதிகள் சர்குலே&#8204;ஷன் என்ற விஜயத்தின் வெற்றியை அவரது திருப்பாதங்களில் சமர்ப்பிக்கிறது. </p>
			<p>இதன் மூலம் பல்லாயிரக்கணக்கான பக்தர்கள், அன்பர்கள், இளைஞர்கள் மாணவ- மாணவியரை தெய்வத்</p>
			<p>திருமூவரின் செய்திகள் சென்றடைகின்றன.</p>
			<p>ஸ்ரீராமகிருஷ்ண விஜயத்தின் சிறப்பு அம்சங்கள் :</p>
			<p>பாரதத்தின் தொன்மையான ஆன்மிகப் பாரம்பரியம், வேதாந்தம் இவை பற்றி தெய்வத்திருமூவர் பாங்கு, வாழ்வின் மூலம் உலகிற்குப் பறைசாற்றுதல்.</p>
			<p>வேதாந்தத்தின் அடிப்படையில் ஆன்மிகமும் விவேகமும் கூடிய வகையில் வாழ்க்கையைக் கண்டு, தம்மைத் தாமே முன்னேற்றிக் கொள்ள உதவும் சுய முன்னேற்றப் பகுதி.</p>
			<p>எளிமையான நடையில் எவருக்கும் விளங்கக்கூடிய வகையில் கருத்துகளை வாசகர்களுக்கு எடுத்துக் கூறுவது.</p>
			<p>ஆசிரியர்கள், மாணவர்கள், அன்னையர் மற்றும் சிறுவர்களுக்கான தனிப்பட்ட ஆர்வங்களுக்கு ஈடு செய்யும் வகையில் தனித்தனிப் பகுதிகள் விஜயத்தில் வெளிவருகின்றன. </p>
			<p>மாணவர்களை எழுச்சி பெறத்தக்க, அவர்களின் ஆற்றலைத் தூண்டி நல்ல இலக்குகளை எட்டும் வண்ணம் பல கட்டுரைகள் வெளியிடப்படுகின்றன. சான்றோர்கள் கட்டுரைகள், பிறமொழிக் கட்டுரைகளின் தமிழாக்கம் போன்றவற்றைப் பழகு தமிழில் அளிக்கிறது விஜயம்.</p>
			<p>படிப்பு, முன்னேற்றம், இலக்கு இவை பற்றிய மாணவர்களின் ஐயங்களுக்கு மாணவர்களுக்கான கேள்வி - பதில் பகுதி, மூலம் சரியான தீர்வு கிடைப்பதோடு தெளிவான மனதுடன் அவர்களை நன்கு சிந்திக்க வைக்கும் பகுதியாகவும் அமைகிறது.</p>
			<p>ஆசிரியர் உலகம்: ‘ஆசிரியர்களே சமுதாயத்தை வடிவமைப்பவர்கள், அவர்களின் ஆற்றல் நாளும் மெருகேற்றப்பட வேண்டும்’ என்பதைக் கருத்தில் கொண்டு அவர்களின் முன்னேற்றம், ஆசிரியர் -மாணவர் உறவு ஆகியவை மேம்படும் வகையில் நல்ல பல கட்டுரைகளைத் தரும் பகுதி இது. </p>
			<p>அன்னையர் உலகம் : குடும்பமே சமுதாயத்தின் மையப்புள்ளி. அது அன்னையின் சக்தியாலேயே இயங்கு</p>
			<p>கிறது. எனவே அன்னையர் தம் உள்ளப்பாங்கு ஆரோக்கியமாக இருந்தால் ஒரு சமுதாயமே நல்ல உருப்பெறும் என்பதற்கிணங்க அன்னையருக்காக வடிவமைக்கப்பட்ட பகுதி. </p>
			<p>பாசம், வீரம், பரிவு, கருணை, தாய் நாட்டுப்பற்று, தியாகம் இவற்றை விளக்கும் அற்புதமான பல கதைகளும் சான்றோர் கருத்துகளும் இதில் இடம் பெறுகின்றன. </p>
			<p>சிறுவர் பகுதி: நமது பாரம்பரியத்தையும், வரலாற்றுச் சிறப்பையும் விளக்கும் வண்ணப்படக்கதைகள். சிறுவர்களுக்கான பொக்கிஷம். சிறுவர்களுக்குக் கதைகள் மூலமே நற்சிந்தனைகளை ஊட்ட முடியும் என்பதால் இந்தப் பகுதி தனிக் கவனத்துடன் சிறப்பாக வெளியிடப்படுகிறது.</p>
			<p>மற்ற அம்சங்கள்: சிரித்து மகிழ ஹாஸ்ய யோகம்</p>
			<p>ஆலயங்களைப் பற்றி அறிந்து கொள்ள புகைப்படப்புதிர்</p>
			<p>பக்கத்துக்குப் பக்கம் பொன்மொழிகள்</p>
			<p>பண்டிகைகளையும், பூஜைகளையும் ஒட்டி வெளியிடப்படும் சுலோகங்கள் என்று விஜயத்தின் பரிமாணங்கள் பரவலாகச் சென்று கொண்டே இருக்கின்றன. </p><br /><br />
			<p>It was in the year 1921 that Swami Sarvananda, a disciple of Swami Vivekananda and the then President of Ramakrishna Math, Madras, initiated the process of propagating the lofty life and messages of Sri Ramakrishna and Swami Vivekananda through a monthly magazine in Tamil.</p>
			<p id="more_english">Swami Sarvananda drafted the dedication and literary proficiency of few of the teachers at RKM Students&apos; Home, Chennai, and sowed the idea of a spiritual magazine in Tamil. Sri. Anna, N. Subramaniam along with his colleague Pandit Mayilvahanan (later Swami Vipulananda) and K.C. Ramaswamy Iyer edited the journal.</p>
			<p>Sri Ramakrishna Vijayam marched along the sands of time steadily and yet slowly in the early years.</p>
			<p>In the first 50 years of Vijayam (1921 – 71 or so), though the circulation was very moderate, the journal was in a formative period. Lofty idealistic contributions by eminent teachers, monks, translation of Gospel of Sri Ramakrishna, lectures of Swamiji as well as noble thoughts from classic Tamil religious scriptures like Thevaram, Divya Prabandam as well as lives of Saints from South were the specialities of Sri Ramakrishna Vijayam. It reached the elite in the field of religion and society. Articles were of very high calibre and the editorials were well received by all.</p>
			<p>The second phase of Vijayam (1975 – 2000) was marked by consolidation of all thoughts, and laid a strong platform for the propagation of life and messages of the Holy Trio. In these 25 years the journal under the editorship of young Swami Kamalatmananda grew leaps and bounds. His systematic approach in updating the journal with pictures, colour transparencies, know&ndash;how from fellow journals of reputed administrative acumen, gave a new look not only to Sri Ramakrishna Vijayam but to the entire scenario of spiritual journalism in Tamil nadu.</p>
			<p>From a mere 1100 copies in 1977, Sri Ramakrishna Vijayam jumped to over 60,000 in the eighties and with inclusion of new stream of life membership of over 20,000 members, it rose up to 91,000. This second phase was appreciated by the entire field of journalists as its growth paved way for origin of new spiritual magazines catering to people of different tastes, cults and sects.</p>
			<p>The Third phase, the current period in Vijayam’s march, began in 2000. A grand function to mark the arrival of the milestone of one lakh subscribers was celebrated in July 2007.</p>
			<p>A plan to reach and benefit every segment of society was conceived. First, the focus shifted on to the Youth. &ldquo;Catch them Young&rdquo; became the theme and schools were the field to be ploughed. Parallel plans were drawn to reach out to other segments to revive the cultural heritage of India in the areas of spirituality, culture, traditions, temples, scriptures, values etc.</p>
			<p>Based on understanding the needs of current readers, longing for in the above mentioned areas of cultural, spiritual and value insights, the quality and content were upgraded. As a result, the circulation accelerated to reach newer heights month on month. The divergence in content appealed to every section of the society, children, youth, parents, office goers, students, teachers, mothers, elders etc. It was a sort of revolution and word of mouth on the change in dimension spreading among the Devotees far and wide. The magazine crossed national boundaries and reached a number of foreign countries.</p>
			<p>Vijayam, today, enjoys the status of being a dear friend, counsellor in hospitals, old age homes, police stations, prisons etc.</p>
			<p>Now, in 2012, the circulation has crossed 1,50,000 by the grace of Bhagawan Sri Ramakrishna.</p><br />
