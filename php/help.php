			<p><span class="color">How to Use the DVD</span><br />
			This DVD provides high-resolution scans of the entire collection of Sri Ramakrishna Vijayam, which can be accessed by clicking on Articles button.
			<p>List of all Article titles sorted alphabetically (click on Articles button)</p>
			<p>Articles of a particular author (click on Authors button)</p>
			<p>Table of Contents of each month (click on Volumes button and select a month)</p>
			<p>List of all Categories (click on Categories button)</p>
			<p>To Search for an article or words (click on Search button)</p>
			<p>Table of contents contain hyperlinks to articles, their authors and the year/month in which they appear. Clicking on a particular article title will take you to the article in DjVu format. Clicking on the author's name will display the list of articles of that particular author, which then can be accessed by clicking on them. Clicking on the year/month will take you to the table of contents of that particular issue.</p>
			
			<p><span class="color">How to search for an article.</span><br />To search for an article click on the Search button and enter details such as Article title and/or its Author and/or the period in which the article might have appeared. On clicking the Search button, search result pertaining to the set of values entered, will be displayed. After opening an article, user can search through it using the plug-in search facility. (Press 'F' and enter the search term. On clicking Find next, the search term, if found will be highlighted in the article.)</p>
			<p><span class="color">How to search for a word.</span><br />To search for a word or phrase in the entire archive click on the search button and enter the word in the 'Text' field. This facility can be used along with the facility provided to search for an article.</p>
			<p><span class="color">How to print a page.</span><br />Open the required page of the archive and click on File > Print. Using the Page Setup tab, printing can be customized. To print a portion of a page, export the page into a .BMP image file (Right click on the page and click on Export). The image can then be cropped  and printed using any image editing tool. To print a picture in the Pictures gallery, click on the thumbnail of the picture. After a full size image is obtained on the screen, click on the printer icon at the bottom of the image.</p>
			<p class="color">Troubleshooting FAQ</p>
			<p>
				<span class="color">Q.</span> AutoRun fails to start the archive.<br />
				<span class="color">A.</span> Click on the file 'Sri_Ramakrishna_Vijayam' in the DVD drive.
			</p>
			<p>
				<span class="color">Q.</span> A runtime error is encountered.<br />
				<span class="color">A.</span> Copy the entire folder 'FirefoxPortable' situated in the DVD drive to C: drive of your computer. Then click on the file 'srv' in the DVD drive.
			</p>
			<p>
				<span class="color">Q.</span> I am able to browse through the Author-Article pages but I am unable to open any page of the archive(DjVu file).<br />
				<span class="color">A.</span> A DjVu plugin-version incompatibility might have occurred. Please install the djvu plugin given in the DVD (Software/DjVuPlugin/djvuctrl-6.1.4-en-r27999.msi)
			</p>
			<p>
				<span class="color">Q.</span> The DVD tray is locked and I am not able to remove the DVD.<br />
				<span class="color">A.</span> The archive might still be active. Right-click on the tray icon 'Sri Ramakrishna Vijayam' and click on 'Close Sri Ramakrishna Vijayam' and then remove the DVD.
			</p>
